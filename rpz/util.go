package rpz

import (
	"math/rand"
)

type Config struct {
	Insecure     bool
	CertFile     string
	KeyFile      string
	PayloadBytes int
}

func RandomString(n int) string {
	const letters = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
	b := make([]byte, n)
	for i := range b {
		b[i] = letters[rand.Intn(len(letters))]
	}
	return string(b)
}
